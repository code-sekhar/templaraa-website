const createProduct = ({
  id,
  slug,
  title,
  author,
  group,
  subCategory,
  price,
  rating,
  image,
  source = "Product Library",
  badge = "",
  sales = "",
  oldPrice = "",
  tags,
  description,
  documentation,
  features,
  includedPages,
}) => {
  const productBase = {
    id,
    slug,
    title,
    author,
    group,
    tech: subCategory,
    subCategory,
    price,
    rating,
    image,
    path: `/products/${slug}`,
    source,
    badge,
    sales,
  };

  return {
    ...productBase,
    oldPrice: oldPrice || Number((price + price * 0.18).toFixed(2)),
    tags: tags || productTagsByGroup[group] || ["html", "react", "template"],
    description: description || getProductShortDescription(productBase),
    documentation: documentation || getProductDocumentation(productBase),
    features:
      features ||
      productFeatures[group] || [
        "Modern, professional design",
        "Fully responsive layout",
        "Easy customization",
        "Fast-loading structure",
        "Clean reusable sections",
      ],
    includedPages:
      includedPages ||
      includedPagesByGroup[group] || [
        "Homepage",
        "About",
        "Services",
        "Details",
        "Contact",
        "404 Page",
      ],
  };
};

